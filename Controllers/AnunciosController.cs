            DateTime fechad = Convert.ToDateTime(anuncio.FechaDesde);
            DateTime fechah = Convert.ToDateTime(anuncio.FechaHasta);
            TimeSpan diff = fechah - fechad;
            int dias = (int)diff.Days;
